public class PreviousAttributes{
    public String status;

	public PreviousAttributes(String status) {
		super();
		this.status = status;
	}

	public PreviousAttributes() {
		super();
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
    
    
}